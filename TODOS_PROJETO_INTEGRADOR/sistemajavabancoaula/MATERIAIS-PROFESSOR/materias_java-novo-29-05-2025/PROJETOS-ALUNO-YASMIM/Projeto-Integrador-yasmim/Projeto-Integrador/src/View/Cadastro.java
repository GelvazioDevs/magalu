    private void btnCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCriarActionPerformed
        String nome = edtNome.getText();
        String telefone = edtTelefone.getText();
        String cpf = edtCPF.getText();
        String email = edtEmail.getText();
        String user = edtUsuario.getText();
        String senha = edtSenha.getText();
        String confsenha = edtConfSenha.getText();

        ModelCadastro cadastro = new ModelCadastro(nome, telefone, cpf, email, user, senha);

        if (!confsenha.contentEquals(senha)) {
            JOptionPane.showMessageDialog(null, "Senhas Incompat√≠veis");
        } else {
            if(cadastrodb.gravarNovoCadastro(cadastro)){
                 JOptionPane.showMessageDialog(null, "Cadastro efetuado com Sucesso!");

                 ////  limpaCampos();
////                    habilitaCampos(false);
////                    edtCodigo.requestFocus();
            }
        }

    }//GEN-LAST:event_btnCriarActionPerformed


